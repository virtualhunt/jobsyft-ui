export async function getServerSideProps(context) {

  var config = {
    method: 'get',
    maxBodyLength: Infinity,
    url: `${process.env.DB_URL}/api/v1/jobs?limit=5&skip=0`,
    headers: {}
  };

  const allJobs = await axios(config)

  return {
    props: {
      jobsData: allJobs.data
    }
  }
}